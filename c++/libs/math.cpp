#include <iostream>
#include <math.h>

using namespace std;

int main()
{
    cout<<pow(2,2)<<endl;

    cout<<fmod(5, 2)<<endl;

    cout<<abs(-4)<<endl;

    cout<<fabs(-4.15)<<endl;

    cout<<floor(3.6)<<endl;

    cout<<ceil(3.4)<<endl;

    cout<<log10(100)<<endl;

    cout<<log(3.4)<<endl;

    cout<<tolower('A')<<endl; // 97

    cout<<toupper('a')<<endl; // 65

    cout<<islower('a')<<endl; // 1

    cout<<isupper('a')<<endl; // 65

    cout<<int('a')<<endl; // 97

    cout<<char(97)<<endl; // 65

}